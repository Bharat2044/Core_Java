package com.bharat.operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 7, b = 11;
    
        // value of a and b
        System.out.println("a is " + a + " and b is " + b);
    
        // == operator
        System.out.println(a + " == " + b + " : " + (a == b)); 
    
        // != operator
        System.out.println(a + " != " + b + " : " + (a != b));  
    
        // > operator
        System.out.println(a + " > " + b + " : " + (a > b));
    
        // < operator
        System.out.println(a + " < " + b + " : " + (a < b));  
    
        // >= operator
        System.out.println(a + " >= " + b + " : " + (a >= b));  
    
        // <= operator
        System.out.println(a + " <= " + b + " : " + (a <= b)); 
    }
}